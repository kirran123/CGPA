export const generateBatchGpaPdf = action({
  args: { batchId: v.string() },
  handler: async (ctx, args) => {
    const records = await ctx.runQuery(api.gpa.getBatchRecords, { batchId: args.batchId });
    if (records.length === 0) throw new Error("Batch empty or not found");

    const batchName = records[0].batchName || args.batchId;
    const buffer = await buildBatchGpaPdfBuffer(records, { batchName });
    return buffer.toString("base64");
  },
});
