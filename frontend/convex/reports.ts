// Rank-list PDF builder
// ══════════════════════════════════════════════════════════════════════════
async function buildRankListPdf(records: any[], meta: any): Promise<Uint8Array> {
  const doc = await PDFDocument.create();
  const bold = await doc.embedFont(StandardFonts.HelveticaBold);
  const reg  = await doc.embedFont(StandardFonts.Helvetica);
  let page = doc.addPage([PW, PH]);

  const label = meta.type === "GPA" ? "GPA" : "CGPA";
  await drawHeader(page, bold, reg, meta.department, `${label} Rank List — Semester ${meta.semester || "All"}`);

  const tableTop = 145;
  fillRect(page, 40, tableTop, PW - 80, 22, C.navy);
  ["Rank", "Register No", "Student Name", label].forEach((h, i) =>
    txt(page, h, [46, 90, 200, 460][i], tableTop + 6, 10, bold, C.white)
  );

  let y = tableTop + 22;
  records.forEach((r: any, idx: number) => {
    const isTop = idx < 3;
    const bg = isTop ? C.yellowLight : (idx % 2 === 0 ? C.lightBg : C.altBg);
    const textColor = isTop ? C.amber : C.dark;
    const f = isTop ? bold : reg;
    if (y > PH - 80) {
      page = doc.addPage([PW, PH]);
      y = 40;
    }
    fillRect(page, 40, y, PW - 80, 20, bg);
    const score = meta.type === "GPA" ? r.gpa : r.cgpa;
    [idx + 1, r.registerNo, r.studentName, (Number(score) || 0).toFixed(2)].forEach((v, i) =>
      txt(page, String(v ?? ""), [46, 90, 200, 460][i], y + 5, 10, f, textColor)
    );
    y += 20;
  });

  drawFooter(page, reg);
  return doc.save();
}

