/* ─── Convex Actions Exposing Base64 Downloads ─── */

export const generateGpaPdf = action({
  args: {
    studentName: v.string(),
    registerNo: v.string(),
    semester: v.number(),
    department: v.string(),
    regulation: v.string(),
    subjects: v.array(
      v.object({
        subjectCode: v.string(),
        subjectName: v.string(),
        credits: v.number(),
        grade: v.string(),
        gradePoint: v.number(),
      })
    ),
    totalCredits: v.number(),
    totalPoints: v.number(),
    gpa: v.number(),
  },
  handler: async (ctx, args) => {
    const buffer = await buildGpaPdfBuffer(args);
    return buffer.toString("base64");
  },
});

export const generateCgpaPdf = action({
  args: {
    studentName: v.string(),
    registerNo: v.string(),
    department: v.string(),
    regulation: v.string(),
    semesters: v.array(
      v.object({
        semester: v.number(),
        gpa: v.number(),
        credits: v.number(),
      })
    ),
    cgpa: v.number(),
  },
  handler: async (ctx, args) => {
    const buffer = await buildCgpaPdfBuffer(args);
    return buffer.toString("base64");
  },
});

export const generateStoredGpaPdf = action({
  args: { recordId: v.id("gpaRecords") },
  handler: async (ctx, args) => {
    const record = await ctx.runQuery(api.gpa.getRecords, {});
    const r = record.find((x: any) => x._id === args.recordId);
    if (!r) throw new Error("GPA record not found");

    const buffer = await buildGpaPdfBuffer(r);
    return buffer.toString("base64");
  },
});

