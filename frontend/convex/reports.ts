// 1. Public GPA PDF (no auth, all fields optional — computed server-side)
export const generateGpaPdf = action({
  args: {
    studentName:  v.optional(v.string()),
    registerNo:   v.optional(v.string()),
    semester:     v.number(),
    department:   v.string(),
    regulation:   v.optional(v.string()),
    subjects: v.array(v.object({
      subjectCode:  v.string(),
      subjectName:  v.string(),
      credits:      v.number(),
      grade:        v.string(),
      gradePoint:   v.optional(v.number()),
    })),
    totalCredits: v.optional(v.number()),
    totalPoints:  v.optional(v.number()),
    gpa:          v.optional(v.number()),
  },
  handler: async (_ctx, args) => {
    const subjects = args.subjects.map((s) => ({
      ...s,
      gradePoint: s.gradePoint ?? DEFAULT_GP[s.grade.trim().toUpperCase()] ?? 0,
    }));
    let totalCredits = args.totalCredits ?? 0;
    let totalPoints  = args.totalPoints  ?? 0;
    if (args.totalCredits === undefined) {
      totalCredits = 0; totalPoints = 0;
      for (const s of subjects) { totalCredits += s.credits; totalPoints += s.credits * s.gradePoint; }
    }
    const gpa = args.gpa ?? (totalCredits > 0 ? parseFloat((totalPoints / totalCredits).toFixed(2)) : 0);
    const bytes = await buildGpaPdf({
      studentName: args.studentName || "ANONYMOUS",
      registerNo:  args.registerNo  || "N/A",
      semester: args.semester, department: args.department,
      regulation: args.regulation || "R2021",
      subjects, totalCredits, totalPoints, gpa,
    });
    return Buffer.from(bytes).toString("base64");
  },
});

// 2. Public CGPA PDF
export const generateCgpaPdf = action({
  args: {
    studentName: v.optional(v.string()),
    registerNo:  v.optional(v.string()),
    department:  v.string(),
    regulation:  v.optional(v.string()),
    semesters: v.array(v.object({
      semester: v.number(),
      gpa:      v.number(),
      credits:  v.optional(v.number()),
    })),
    cgpa: v.optional(v.number()),
  },
  handler: async (_ctx, args) => {
    const semesters = args.semesters.map((s) => ({ ...s, credits: s.credits ?? 0 }));
    let sum = 0, n = 0;
    for (const s of semesters) { if (s.gpa > 0) { sum += s.gpa; n++; } }
    const cgpa = args.cgpa ?? (n > 0 ? parseFloat((sum / n).toFixed(2)) : 0);
    const bytes = await buildCgpaPdf({
      studentName: args.studentName || "ANONYMOUS",
      registerNo:  args.registerNo  || "N/A",
      department: args.department,
      regulation: args.regulation || "R2021",
      semesters, cgpa,
    });
    return Buffer.from(bytes).toString("base64");
  },
});

// 3. Stored GPA record PDF (fetch by ID then generate)
export const generateStoredGpaPdf = action({
  args: { recordId: v.id("gpaRecords") },
  handler: async (ctx, args) => {
    const r = await ctx.runQuery(api.gpa.getById, { id: args.recordId });
    if (!r) throw new Error("GPA record not found");
    const cgpaRec = await ctx.runQuery(api.cgpa.getByRegNo, { registerNo: r.registerNo, department: r.department });
    const semesters = cgpaRec?.semesters && cgpaRec.semesters.length > 0
      ? cgpaRec.semesters
      : [{ semester: r.semester, gpa: r.gpa, credits: r.totalCredits || 0 }];
    const bytes = await buildGpaPdf({
      studentName: r.studentName,
      registerNo: r.registerNo,
      department: r.department,
      regulation: r.regulation || "R2021",
      semester: r.semester,
      gpa: r.gpa,
      semesters,
      cgpa: cgpaRec?.cgpa || r.gpa,
    });
    return Buffer.from(bytes).toString("base64");
  },
});

