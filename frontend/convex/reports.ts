// Default grade map (fallback when gradePoint not in record)
// ══════════════════════════════════════════════════════════════════════════
const DEFAULT_GP: Record<string, number> = {
  O: 10, "A+": 9, A: 8, "B+": 7, B: 6, C: 5, U: 0, RA: 0,
};

