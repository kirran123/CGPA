export const generateRankListGpaPdf = action({
  args: {
    department: v.string(),
    semester: v.number(),
  },
  handler: async (ctx, args) => {
    const records = await ctx.runQuery(api.gpa.getRecords, {
      department: args.department,
      semester: args.semester,
    });

    const sorted = [...records].sort((a: any, b: any) => b.gpa - a.gpa).slice(0, 100);

    const buffer = await buildRankListPdfBuffer(sorted, {
      department: args.department,
      semester: args.semester,
      type: "GPA",
    });
    return buffer.toString("base64");
  },
});

