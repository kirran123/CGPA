// ── Coordinate helper: convert pdfkit-style y (top-left) to pdf-lib (bottom-left)
const pY = (y: number, h: number = 0) => PH - y - h;

// ── Draw filled rectangle ──────────────────────────────────────────────────
function fillRect(page: any, x: number, y: number, w: number, h: number, color: any) {
  page.drawRectangle({ x, y: pY(y, h), width: w, height: h, color });
}

// ── Draw text at pdfkit-style coordinates ─────────────────────────────────
function txt(
  page: any,
  text: string,
  x: number,
  y: number,
  size: number,
  font: any,
  color: any = C.dark,
  maxWidth?: number,
  align: "left" | "center" = "left"
) {
  let drawX = x;
  const safe = String(text ?? "");
  if (align === "center" && maxWidth !== undefined) {
    const tw = font.widthOfTextAtSize(safe, size);
    drawX = x + Math.max(0, (maxWidth - tw) / 2);
  }
  page.drawText(safe, {
    x: drawX,
    y: pY(y, size * 0.75),
    size,
    font,
    color,
    maxWidth,
  });
}

